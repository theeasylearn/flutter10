void main() {
  //create read only variables
  //using final keyword
  //without datatype
  final city = "Bhavnagar";
  //with datatype
  final double birthdate = 12.7;

  //using const keyword
  //without type
  const isMarried = false;
  //with type
  const int year = 2025;

  print(city);
  print(birthdate);
  print(isMarried);
  print(year);
}
